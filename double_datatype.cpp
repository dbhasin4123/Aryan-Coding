#include <iostream>
using namespace std;

int main() {

    //  Printing an double variable
    double a =10.999;
    cout << a << endl;

    // Adding a double variable to another double variable
    double b = 20.9998;
    cout << a + b << endl;

    // subtracting a double variable from another  variable
    cout << a - b << endl;

    // multiplying a double variable with another double variable
    cout << a * b << endl;

    // dividing a double variable with another double variable
    cout << b / a << endl;

    return 0;
}
