#include <iostream>
using namespace std;

int main() {

    //  Printing an integer variable
    int a =10;
    cout << a << endl;

    // Adding a integer variable to another integer variable
    int b = 20;
    cout << a + b << endl;

    // subtracting a integer variable from another integer variable
    cout << a - b << endl;

    // multiplying a integer variable with another integer variable
    cout << a * b << endl;

    // dividing a integer variable with another integer variable
    cout << b / a << endl;

    // adding three variable at a time using integer variable
    int x=10, y=20 ,z=40;
    cout << x+y+z << endl;

    // adding same variables using integer variable
    int n,m;
    n=m=12;
    cout << n+m << endl;

    return 0;
}
