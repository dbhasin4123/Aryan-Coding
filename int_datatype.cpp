#include <iostream>
using namespace std;

int main() {

    //  Printing an integer variable
    int a =10;
    cout << a << endl;

    // Adding a integer variable to another integer variable
    int b = 20;
    cout << a + b << endl;

    // subtracting a integer variable from another integer variable
    cout << a - b << endl;

    // multiplying a integer variable with another integer variable
    cout << a * b << endl;

    // dividing a integer variable with another integer variable
    cout << b / a << endl;

    return 0;
}
